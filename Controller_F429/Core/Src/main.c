void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	if(hadc == &hadc1){
		AD_RES_1 = HAL_ADC_GetValue(&hadc1);
		controller_U.h_ref = ((float)(AD_RES_1)/4095.0)*pos_max;
	}
	if(hadc == &hadc2){
		AD_RES_2 = HAL_ADC_GetValue(&hadc2);
		motor_current_Y = i_in_offset + i_in_slope*AD_RES_2 + i_in_correction;
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim)
{
	if(htim == &htim1){
		int32_t increment = TIM8->CNT - ENCODER_CNT_MIDDLE;
		TIM8->CNT = ENCODER_CNT_MIDDLE;
		encoder_speed = increment;

		motor_speed_Y = encoder_speed*enc_multiplier;
		mass_speed_Y = motor_speed_Y*r_shaft;

		HAL_ADC_Start_IT(&hadc1);
		HAL_ADC_Start_IT(&hadc2);

		controller_U.mot_amp = motor_current_Y;
		controller_U.shft_pos = motor_position_Y;
		controller_U.shft_vel = motor_speed_Y;
		controller_U.m_pos = mass_position_Y;
		controller_U.m_vel = mass_speed_Y;

		controller_step();

		voltage_U = controller_Y.Ug_U;

		TIM3->CCR1 = (int)(v_out_offset + v_out_slope*voltage_U);

		HAL_GPIO_TogglePin(GPIOG, GPIO_PIN_14);
	}
}

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim){
	if(htim == &htim4){
		if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)  // If the interrupt is triggered by channel 1
		{
			ICValue = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);

			pwm_input_duty = (float)(((float)HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2))/(float)ICValue);

			mass_position_Y = h_in_offset + h_in_slope*pwm_input_duty;

			if(mass_position_Y > pos_max){mass_position_Y = pos_max;}
			if(mass_position_Y < pos_min){mass_position_Y = pos_min;}

			motor_position_Y = mass_position_Y/r_shaft;

		}
	}
	HAL_GPIO_TogglePin(GPIOG, GPIO_PIN_13);
}
